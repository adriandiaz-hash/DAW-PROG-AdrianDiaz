    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
     if (!chkResuelta.isSelected()) {
            JOptionPane.showMessageDialog(this, "Debes marcar el checkbox para confirmar que la tarea está resuelta.", "Falta confirmación", JOptionPane.WARNING_MESSAGE);
        } else {
            int codigoBuscado = Integer.parseInt(txtCodigoActualizar.getText().trim());
            boolean actualizada = false;
            int i = 0;
            java.util.ArrayList<Tarea> tareas = controlador.obtenerTodasLasTareas();

            while (i < tareas.size() && !actualizada) {
                Tarea t = tareas.get(i);
                if (t.getCodigo() == codigoBuscado) {
                    t.setEstado("Finalizada");
                    controlador.actualizarMemoria();
                    actualizada = true;
                    
                    JOptionPane.showMessageDialog(this, "Tarea [" + codigoBuscado + "] actualizada a Finalizada correctamente.", "Actualización Exitosa", JOptionPane.INFORMATION_MESSAGE);
                    
                    txtCodigoActualizar.setText("");
                    lbInfoTarea.setText("Esperando búsqueda...");
                    chkResuelta.setSelected(false);
                    chkResuelta.setEnabled(false);
                    btnActualizar.setEnabled(false);
                    btnEliminar.setEnabled(false);
                    
                    actualizarTabla(tablePendientes, "Pendiente");
                }
                i++;
            }
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

