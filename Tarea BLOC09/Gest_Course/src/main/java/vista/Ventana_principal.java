    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int codigoBuscado = Integer.parseInt(txtCodigoActualizar.getText().trim());

    int confirmacion = JOptionPane.showConfirmDialog(this, 
        "¿Estás seguro de que deseas eliminar permanentemente la tarea [" + codigoBuscado + "]?", 
        "Confirmar Eliminación", 
        JOptionPane.YES_NO_OPTION, 
        JOptionPane.WARNING_MESSAGE);

    if (confirmacion == JOptionPane.YES_OPTION) {
        boolean exito = controlador.eliminarTarea(codigoBuscado);
        
        if (exito) {
            JOptionPane.showMessageDialog(this, "Tarea eliminada de los registros.", "Purga completada", JOptionPane.INFORMATION_MESSAGE);

            txtCodigoActualizar.setText("");
            lbInfoTarea.setText("Esperando búsqueda...");
            chkResuelta.setSelected(false);
            chkResuelta.setEnabled(false);
            btnActualizar.setEnabled(false);
            btnEliminar.setEnabled(false);

            actualizarTabla(tablePendientes, "Pendiente");
            actualizarTabla(tableFinalizadas, "Finalizada");
        } else {
            JOptionPane.showMessageDialog(this, "Error: No se pudo localizar la tarea para su eliminación.", "Fallo del sistema", JOptionPane.ERROR_MESSAGE);
        }
    }
    }//GEN-LAST:event_btnEliminarActionPerformed

