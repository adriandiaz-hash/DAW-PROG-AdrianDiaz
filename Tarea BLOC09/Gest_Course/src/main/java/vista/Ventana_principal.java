    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String codigoStr = txtCodigoActualizar.getText().trim();
    
        if (codigoStr.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, introduce un código para buscar.", "Campo vacío", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                int codigoBuscado = Integer.parseInt(codigoStr);
                boolean encontrada = false;
                int i = 0;
                java.util.ArrayList<Tarea> tareas = controlador.obtenerTodasLasTareas();

                while (i < tareas.size() && !encontrada) {
                    Tarea t = tareas.get(i);
                    if (t.getCodigo() == codigoBuscado) {
                        encontrada = true;
                        lbInfoTarea.setText("<html><b>Título:</b> " + t.getTitulo() + "<br><b>Módulo:</b> " + t.getModulo() + "<br><b>Estado actual:</b> " + t.getEstado() + "</html>");
                        
                        if (t.getEstado().equals("Finalizada")) {
                            chkResuelta.setSelected(true);
                            chkResuelta.setEnabled(false);
                            btnActualizar.setEnabled(false);
                            btnEliminar.setEnabled(true);
                            JOptionPane.showMessageDialog(this, "Esta tarea ya consta como resuelta en los registros.", "Aviso", JOptionPane.INFORMATION_MESSAGE);
                        } else {
                            chkResuelta.setSelected(false);
                            chkResuelta.setEnabled(true);
                            btnActualizar.setEnabled(true);
                            btnEliminar.setEnabled(true);
                        }
                    }
                    i++;
                }

                if (!encontrada) {
                    JOptionPane.showMessageDialog(this, "No se ha encontrado ninguna tarea con el código [" + codigoBuscado + "].", "Búsqueda fallida", JOptionPane.ERROR_MESSAGE);
                    lbInfoTarea.setText("Esperando búsqueda...");
                    chkResuelta.setEnabled(false);
                    btnActualizar.setEnabled(false);
                }

            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "El código debe ser un valor numérico.", "Error de formato", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

