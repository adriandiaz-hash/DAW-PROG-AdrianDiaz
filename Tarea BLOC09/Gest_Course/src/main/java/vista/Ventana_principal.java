    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if (txtTitulo.getText().trim().isEmpty() || txtFechaEntrega.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, rellena el título y la fecha de entrega.", "Error de validación", JOptionPane.ERROR_MESSAGE);
        } else if (!rbAlta.isSelected() && !rbMedia.isSelected() && !rbBaja.isSelected()) {
            JOptionPane.showMessageDialog(this, "Debes seleccionar una prioridad.", "Error de validación", JOptionPane.ERROR_MESSAGE);
        } else {
            String titulo = txtTitulo.getText().trim();
            String modulo = cbModulo.getSelectedItem().toString();
            String fechaCre = txtFechaCreacion.getText();
            String fechaEnt = txtFechaEntrega.getText().trim();
            
            String prioridad = "";
            if (rbAlta.isSelected()) prioridad = "Alta";
            else if (rbMedia.isSelected()) prioridad = "Media";
            else if (rbBaja.isSelected()) prioridad = "Baja";

            int nuevoCodigo = 1;
            for (Tarea t : controlador.obtenerTodasLasTareas()) {
                if (t.getCodigo() >= nuevoCodigo) {
                    nuevoCodigo = t.getCodigo() + 1;
                }
            }

            Tarea nuevaTarea = new Tarea(nuevoCodigo, titulo, prioridad, modulo, fechaCre, fechaEnt);
            controlador.registrarTarea(nuevaTarea);

            JOptionPane.showMessageDialog(this, "Tarea guardada con éxito. Código asignado: " + nuevoCodigo, "Éxito", JOptionPane.INFORMATION_MESSAGE);

            txtTitulo.setText("");
            txtFechaEntrega.setText("");
            buttonGroup1.clearSelection();
            cbModulo.setSelectedIndex(0);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

