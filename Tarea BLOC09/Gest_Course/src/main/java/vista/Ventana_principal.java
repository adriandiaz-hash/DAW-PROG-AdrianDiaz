    private void actualizarTabla(javax.swing.JTable tablaDestino, String estadoFiltro) {
        String[] columnas = {"Código", "Título", "Prioridad", "Módulo", "F. Creación", "F. Entrega", "Estado"};
        
        javax.swing.table.DefaultTableModel modeloTabla = new javax.swing.table.DefaultTableModel(columnas, 0) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false; 
            }
        };
        
        for (Tarea t : controlador.obtenerTodasLasTareas()) {
            if (t.getEstado().equals(estadoFiltro)) {
                Object[] fila = {
                    t.getCodigo(),
                    t.getTitulo(),
                    t.getPrioridad(),
                    t.getModulo(),
                    t.getFechaCreacion(),
                    t.getFechaEntrega(),
                    t.getEstado()
                };
                modeloTabla.addRow(fila);
            }
        }
        
        tablaDestino.setModel(modeloTabla);
    }

